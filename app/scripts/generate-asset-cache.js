#!/usr/bin/env node
/* eslint-disable no-console */

/**
 * Static Asset Cache Generation Script
 *
 * Fingerprints the hand-authored assets that are referenced by URL at runtime —
 * images, sounds, slug-addressed icon sets (lessons/badges/challenges), and a few
 * loose icons — so they can be served immutably from R2 instead of the worker.
 * For each source file it writes a content-hashed copy under the shared hashed
 * tree and records the mapping in one manifest:
 *
 *   public/static/hashed/assets/{relpath}/{name}-{hash}.{ext}
 *     - Fingerprinted copies (gitignored, uploaded immutably to R2)
 *
 *   lib/generated/asset-hashes.ts
 *     - Manifest: "{relpath}" -> "/static/hashed/assets/{relpath}/{name}-{hash}.{ext}"
 *       Keys are relative to /static (e.g. "images/logo.png", "icons/lessons/foo.svg").
 *
 * Consumed via `staticAsset()` (lib/static-asset.ts), which resolves a relative
 * path to its hashed URL and wraps it in assetsUrl(). CSS-referenced assets are
 * hashed separately into hashed/css by generate-css-asset-hashes.js (they're
 * rewritten in stylesheets, not resolved at runtime). NOT hashed here: Lottie
 * animations + SVGR icons (bundled into JS), theme-script.js (first-paint, kept
 * same-origin), favicon.ico (pinned to the site root), and concept icons (part of
 * the concept content tree).
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { computeHash, writeFile } from "./lib/cache-utils.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const STATIC_DIR = path.join(__dirname, "../public/static");
const ICONS_SRC_DIR = path.join(__dirname, "../icons"); // app/icons (symlinked to curriculum)
const OUT_DIR = path.join(STATIC_DIR, "hashed", "assets");
const GENERATED_DIR = path.join(__dirname, "../lib/generated");

// Whole source directories under public/static to fingerprint.
const SOURCE_DIRS = ["images", "sounds"];
// Individual loose files under public/static referenced by URL.
const SOURCE_FILES = ["robot.png", "icons/move.svg", "icons/user-fallback.svg"];
// Slug-addressed icon sets from app/icons/{dir}, emitted under "icons/{dir}/".
const ICON_SETS = ["lessons", "badges", "challenges"];

/** Recursively collect file paths under a dir, relative to a base, skipping dotfiles. */
function walk(absDir, relDir, out = []) {
  for (const entry of fs.readdirSync(absDir, { withFileTypes: true })) {
    if (entry.name.startsWith(".")) {
      continue;
    }
    const abs = path.join(absDir, entry.name);
    const rel = relDir ? `${relDir}/${entry.name}` : entry.name;
    if (entry.isDirectory()) {
      walk(abs, rel, out);
    } else if (entry.isFile()) {
      out.push({ abs, rel });
    }
  }
  return out;
}

/**
 * Fingerprint one source file. `srcAbs` is the file on disk; `key` is its
 * /static-relative manifest key. Returns { key, url }.
 */
function hashOne(srcAbs, key) {
  const bytes = fs.readFileSync(srcAbs);
  const hash = computeHash(bytes);
  const ext = path.extname(key);
  const base = ext ? key.slice(0, -ext.length) : key;
  const outRel = `${base}-${hash}${ext}`;
  writeFile(path.join(OUT_DIR, outRel), bytes);
  return { key, url: `/static/hashed/assets/${outRel}` };
}

function writeManifest(entries) {
  const body = entries
    .sort((a, b) => a.key.localeCompare(b.key))
    .map((e) => `  ${JSON.stringify(e.key)}: ${JSON.stringify(e.url)}`)
    .join(",\n");

  const content = `// Auto-generated by scripts/generate-asset-cache.js — DO NOT EDIT
export const assetHashes: Record<string, string> = {
${body}
};
`;
  writeFile(path.join(GENERATED_DIR, "asset-hashes.ts"), content);
}

function generateAssetCache() {
  console.log("Generating static asset cache...\n");

  if (fs.existsSync(OUT_DIR)) {
    fs.rmSync(OUT_DIR, { recursive: true });
  }
  fs.mkdirSync(OUT_DIR, { recursive: true });
  fs.mkdirSync(GENERATED_DIR, { recursive: true });

  const entries = [];

  // Whole public/static source dirs (keys relative to /static).
  for (const dir of SOURCE_DIRS) {
    const absDir = path.join(STATIC_DIR, dir);
    if (fs.existsSync(absDir)) {
      for (const { abs, rel } of walk(absDir, dir)) {
        entries.push(hashOne(abs, rel));
      }
    }
  }

  // Loose public/static files.
  for (const file of SOURCE_FILES) {
    const abs = path.join(STATIC_DIR, file);
    if (fs.existsSync(abs)) {
      entries.push(hashOne(abs, file));
    } else {
      console.warn(`   Warning: source file not found, skipping: ${file}`);
    }
  }

  // Slug-addressed icon sets from app/icons/{set}, keyed as "icons/{set}/{slug}.svg".
  for (const set of ICON_SETS) {
    const srcDir = path.join(ICONS_SRC_DIR, set);
    if (!fs.existsSync(srcDir)) {
      throw new Error(`Icon set directory not found: ${srcDir}`);
    }
    for (const { abs, rel } of walk(srcDir, `icons/${set}`)) {
      if (rel.endsWith(".svg")) {
        entries.push(hashOne(abs, rel));
      }
    }
  }

  writeManifest(entries);

  console.log("Static asset cache generated successfully:\n");
  console.log(`   Assets: ${entries.length}`);
  console.log(`   Output: ${OUT_DIR}\n`);
}

try {
  generateAssetCache();
} catch (error) {
  console.error("Failed to generate static asset cache:", error);
  process.exit(1);
}

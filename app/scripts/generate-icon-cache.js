#!/usr/bin/env node
/* eslint-disable no-console */

/**
 * Icon Cache Generation Script
 *
 * Content-hashes the slug-addressed icon sets (lessons, badges, projects) so
 * they can be served immutably. For each icon it copies the source SVG to a
 * fingerprinted filename and records the URL in a manifest:
 *
 *   public/static/icons-hashed/{type}/{slug}-{hash}.svg
 *     - Fingerprinted copies (served immutable — see public/_headers)
 *
 *   lib/generated/icon-hashes.ts
 *     - Per-type slug -> URL manifests + fallback URLs
 *
 * Consumed by the LessonIcon / BadgeIcon / ProjectIcon components, which look
 * up the hashed URL by slug and fall back to the hashed fallback icon.
 *
 * Concept icons are handled separately by generate-concept-cache.js (their URL
 * is also needed as the concept OG image), following the same pattern.
 */

import fs from "fs";
import path from "path";
import crypto from "crypto";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ICONS_DIR = path.join(__dirname, "../icons");
const STATIC_DIR = path.join(__dirname, "../public/static/icons-hashed");
const GENERATED_DIR = path.join(__dirname, "../lib/generated");

// Icon sets addressed by slug via <img src>. Each entry maps to app/icons/{dir}
// (symlinked to the curriculum source) and produces a `{export}Urls` manifest.
const ICON_SETS = [
  { dir: "lessons", exportName: "lesson" },
  { dir: "badges", exportName: "badge" },
  { dir: "projects", exportName: "project" }
];

const FALLBACK_SLUG = "fallback";

/**
 * Compute a 12-char SHA-256 hash of content
 */
function computeHash(content) {
  return crypto.createHash("sha256").update(content).digest("hex").slice(0, 12);
}

/**
 * Write a file, creating directories as needed
 */
function writeFile(filePath, content) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, content);
}

/**
 * Copy an icon to a content-hashed filename and return its immutable public URL.
 */
function copyHashedIcon(dir, slug, srcPath) {
  const bytes = fs.readFileSync(srcPath);
  const hash = computeHash(bytes);
  const outName = `${slug}-${hash}.svg`;
  writeFile(path.join(STATIC_DIR, dir, outName), bytes);
  return `/static/icons-hashed/${dir}/${outName}`;
}

/**
 * Process one icon set, returning { urls: { [slug]: url }, fallbackUrl }.
 */
function processIconSet({ dir }) {
  const srcDir = path.join(ICONS_DIR, dir);
  if (!fs.existsSync(srcDir)) {
    throw new Error(`Icon directory not found: ${srcDir}`);
  }

  const svgFiles = fs.readdirSync(srcDir, { withFileTypes: true }).filter((f) => f.isFile() && f.name.endsWith(".svg"));

  const urls = {};
  let fallbackUrl = null;

  for (const file of svgFiles) {
    const slug = path.basename(file.name, ".svg");
    const url = copyHashedIcon(dir, slug, path.join(srcDir, file.name));
    if (slug === FALLBACK_SLUG) {
      fallbackUrl = url;
    } else {
      urls[slug] = url;
    }
  }

  if (!fallbackUrl) {
    throw new Error(`Icon set "${dir}" is missing a ${FALLBACK_SLUG}.svg`);
  }

  return { urls, fallbackUrl };
}

/**
 * Write the TypeScript manifest of per-type slug -> URL maps and fallbacks.
 */
function writeManifest(sets) {
  const blocks = ICON_SETS.map(({ exportName }) => {
    const { urls, fallbackUrl } = sets[exportName];
    const entries = Object.entries(urls)
      .sort(([a], [b]) => a.localeCompare(b))
      .map(([slug, url]) => `  ${JSON.stringify(slug)}: ${JSON.stringify(url)}`)
      .join(",\n");

    return (
      `export const ${exportName}IconUrls: Record<string, string> = {\n${entries}\n};\n\n` +
      `export const ${exportName}IconFallbackUrl = ${JSON.stringify(fallbackUrl)};`
    );
  }).join("\n\n");

  const content = `// Auto-generated by scripts/generate-icon-cache.js — DO NOT EDIT\n${blocks}\n`;
  writeFile(path.join(GENERATED_DIR, "icon-hashes.ts"), content);
}

/**
 * Main generation function
 */
function generateIconCache() {
  console.log("Generating icon cache...\n");

  // Clean output directory
  if (fs.existsSync(STATIC_DIR)) {
    fs.rmSync(STATIC_DIR, { recursive: true });
  }
  fs.mkdirSync(STATIC_DIR, { recursive: true });
  fs.mkdirSync(GENERATED_DIR, { recursive: true });

  const sets = {};
  for (const set of ICON_SETS) {
    sets[set.exportName] = processIconSet(set);
  }

  writeManifest(sets);

  console.log("Icon cache generated successfully:\n");
  for (const { dir, exportName } of ICON_SETS) {
    console.log(`   ${dir}: ${Object.keys(sets[exportName].urls).length} icons`);
  }
  console.log(`   Output: ${STATIC_DIR}\n`);
}

// Run generation
try {
  generateIconCache();
} catch (error) {
  console.error("Failed to generate icon cache:", error);
  process.exit(1);
}

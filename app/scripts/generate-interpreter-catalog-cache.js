#!/usr/bin/env node
/* eslint-disable no-console */

/**
 * Interpreter Catalog Cache Generation Script
 *
 * The interpreters OWN and author their student-facing diagnostic strings, but
 * must not bundle the whole locale set into the runtime (inject-the-dict model).
 * This script exports each interpreter's per-locale message catalog so the app
 * can fetch the active locale's dict at runtime and inject it via
 * `EvaluationContext.localeMessages`.
 *
 * Reads interpreter locale sources and produces:
 *
 *   public/static/interpreter-catalogs/{language}-{locale}-{hash}.json
 *     - One interpreter's message catalog for one human locale
 *
 *   lib/generated/interpreter-catalog-hashes.ts
 *     - Hash manifest: language -> locale -> catalog hash
 *
 * The `system` pseudo-locale is intentionally NOT exported — it is the bundled
 * default inside each interpreter (a loud canary when nothing is injected), never
 * fetched. Uploaded to R2 automatically by `static:upload` (public/static sync).
 */

import fs from "fs";
import path from "path";
import crypto from "crypto";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const INTERPRETERS_SRC = path.join(__dirname, "../../interpreters/src");
const STATIC_DIR = path.join(__dirname, "../public/static/interpreter-catalogs");
const GENERATED_DIR = path.join(__dirname, "../lib/generated");

const LANGUAGES = ["javascript", "python", "jikiscript"];

// The pseudo-locale bundled inside each interpreter as the no-injection default —
// never fetched, so never exported.
const EXCLUDED_LOCALES = new Set(["system"]);

function computeHash(content) {
  return crypto.createHash("sha256").update(content).digest("hex").slice(0, 12);
}

function writeFile(filePath, content) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, content);
}

/**
 * Build catalog files (per language/locale). Returns the hash manifest:
 * { [language]: { [locale]: hash } }.
 */
function buildCatalogFiles() {
  const hashes = {};

  for (const language of LANGUAGES) {
    const localesDir = path.join(INTERPRETERS_SRC, language, "locales");
    if (!fs.existsSync(localesDir)) {
      console.warn(`   Warning: no locales dir for "${language}" — skipping`);
      continue;
    }

    const localeDirs = fs
      .readdirSync(localesDir, { withFileTypes: true })
      .filter((d) => d.isDirectory() && !EXCLUDED_LOCALES.has(d.name));

    for (const localeDir of localeDirs) {
      const locale = localeDir.name;
      const catalogPath = path.join(localesDir, locale, "translation.json");
      if (!fs.existsSync(catalogPath)) {
        continue;
      }

      // Parse + re-stringify to a normalized, compact form so the hash is stable
      // regardless of source formatting.
      let catalog;
      try {
        catalog = JSON.parse(fs.readFileSync(catalogPath, "utf-8"));
      } catch (error) {
        throw new Error(`Invalid JSON in ${catalogPath}: ${error.message}`);
      }

      const content = JSON.stringify(catalog);
      const hash = computeHash(content);

      if (!hashes[language]) {
        hashes[language] = {};
      }
      hashes[language][locale] = hash;

      writeFile(path.join(STATIC_DIR, `${language}-${locale}-${hash}.json`), content);
    }
  }

  return hashes;
}

/**
 * Write the TypeScript hash manifest (language -> locale -> hash), deterministically ordered.
 */
function writeHashManifest(hashes) {
  const languages = Object.keys(hashes).sort();
  const body = languages
    .map((language) => {
      const locales = Object.keys(hashes[language]).sort();
      const inner = locales
        .map((locale) => `    ${JSON.stringify(locale)}: ${JSON.stringify(hashes[language][locale])}`)
        .join(",\n");
      return `  ${JSON.stringify(language)}: {\n${inner}\n  }`;
    })
    .join(",\n");

  const content = `// Auto-generated by scripts/generate-interpreter-catalog-cache.js — DO NOT EDIT
export const interpreterCatalogHashes: Record<string, Record<string, string>> = {
${body}
};
`;

  writeFile(path.join(GENERATED_DIR, "interpreter-catalog-hashes.ts"), content);
}

function generateInterpreterCatalogCache() {
  console.log("Generating interpreter catalog cache...\n");

  if (fs.existsSync(STATIC_DIR)) {
    fs.rmSync(STATIC_DIR, { recursive: true });
  }
  fs.mkdirSync(STATIC_DIR, { recursive: true });
  fs.mkdirSync(GENERATED_DIR, { recursive: true });

  const hashes = buildCatalogFiles();
  writeHashManifest(hashes);

  let fileCount = 0;
  for (const language of Object.keys(hashes)) {
    fileCount += Object.keys(hashes[language]).length;
  }

  console.log("Interpreter catalog cache generated successfully:\n");
  for (const language of Object.keys(hashes).sort()) {
    console.log(`   ${language}: ${Object.keys(hashes[language]).sort().join(", ")}`);
  }
  console.log(`   Catalog files: ${fileCount}`);
  console.log(`   Output: ${STATIC_DIR}\n`);
}

try {
  generateInterpreterCatalogCache();
} catch (error) {
  console.error("Failed to generate interpreter catalog cache:", error);
  process.exit(1);
}
